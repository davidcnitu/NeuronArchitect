    //Calculate configuration index based on the position within the blueprint 
    public static int GetCubeConfiguration(int z, int y, int x, List<int[,]> blueprint)
    {
        int configurationIndex = 0;

        for (int i = 0; i < 8; i++)
        {
            if (blueprint[z + (int)CubeVertices[i][2]][y + (int)CubeVertices[i][1], x + (int)CubeVertices[i][0]] == 1)
            {
                configurationIndex += (int)System.Math.Pow(2, i);
            }
        }
        return configurationIndex;
    }

    //Calculate and add vertices and triangles for a given location within the blueprint
    public static void MarchCube(Vector3 position, int configurationIndex, List<Vector3> vertices, List<int> triangles)
    {
        int edgeIndex = 0;

        if (configurationIndex == 0 || configurationIndex == 255)
        {
            return;
        }

        for (int triangleNumber = 0; triangleNumber < 5; triangleNumber++)
        {
            for (int triangleEdge = 0; triangleEdge < 3; triangleEdge++)
            {
                int indice = ConfigurationTable[configurationIndex, edgeIndex];

                if (indice == -1)
                {
                    return;
                }

                Vector3 newVertex = ((position + CubeEdges[indice, 0]) + (position + CubeEdges[indice, 1])) / 2f;

                vertices.Add(newVertex);
                triangles.Add(vertices.Count - 1);
                edgeIndex++;
            }
        }
    }
   
